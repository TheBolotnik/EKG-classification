# Переводы диагностических суперклассов PTB-XL на русский
CLASS_RU = {
    "NORM": "Норма",
    "MI":   "Инфаркт миокарда",
    "STTC": "Изменения сегмента ST/Т",
    "CD":   "Нарушения проводимости",
    "HYP":  "Гипертрофия",
}


def to_russian(label: str) -> str:
    """Вернуть русский перевод для суперкласса PTB-XL; если не знаем — вернуть исходник."""
    return CLASS_RU.get(label, label)
